// Regenerate the ATT&CK technique dataset from MITRE's official Enterprise STIX
// bundle so names, IDs, tactics, URLs, and definitions are authoritative (never
// hand-invented).
//
//   node scripts/gen-attack-map.mjs <path-to-enterprise-attack.json>
//
// Source bundle (≈50 MB, not committed):
//   https://raw.githubusercontent.com/mitre-attack/attack-stix-data/master/enterprise-attack/enterprise-attack.json
//
// Emits two files under src/data/:
//   attack-techniques.generated.ts — every base technique (+ any curated
//       sub-technique) as { id, name, tactics[], url, summary }, summary = the
//       trimmed MITRE description. AUTO-GENERATED; do not hand-edit.
//   attack-overlay.ts — curated glossary links + bespoke summary/significance/
//       example, keyed by technique id. Hand-maintained; merged in references.ts.
//       Regenerated here ONLY when it doesn't exist yet (first migration); on
//       later runs the existing curated overlay is preserved.
import fs from 'node:fs';
import path from 'node:path';

const ROOT = process.cwd();
const STIX = process.argv[2] || path.join(ROOT, 'enterprise-attack.json');
const data = JSON.parse(fs.readFileSync(STIX, 'utf8'));
const objects = data.objects || [];

// shortname → display name, from the canonical tactic objects.
const tacticName = {};
const tacticById = {};
for (const o of objects) {
  if (o.type === 'x-mitre-tactic' && !o.revoked) {
    if (o.x_mitre_shortname) tacticName[o.x_mitre_shortname] = o.name;
    tacticById[o.id] = o.name;
  }
}
// Authoritative kill-chain order straight from the matrix object's tactic_refs.
const matrix = objects.find((o) => o.type === 'x-mitre-matrix' && !o.revoked);
const tacticOrder = matrix ? (matrix.tactic_refs || []).map((id) => tacticById[id]).filter(Boolean) : [];

// Trim a MITRE markdown description to one clean sentence for the card/definition.
function clean(desc) {
  if (!desc) return '';
  let s = String(desc)
    .replace(/\(Citation:[^)]*\)/g, '')
    .replace(/\[([^\]]+)\]\([^)]*\)/g, '$1')
    .replace(/<\/?code>/g, '')
    .replace(/[*`]/g, '')
    .replace(/\s+/g, ' ')
    .trim();
  const parts = s.split(/(?<=\.)\s+/);
  let out = parts[0] || s;
  if (out.length < 60 && parts[1]) out += ' ' + parts[1];
  if (out.length > 240) out = out.slice(0, 237).replace(/\s+\S*$/, '') + '…';
  return out;
}
function ref(o) {
  const r = (o.external_references || []).find((x) => x.source_name === 'mitre-attack' && x.external_id);
  return r ? { id: r.external_id, url: r.url } : null;
}

const byId = {};
for (const o of objects) {
  if (o.type !== 'attack-pattern' || o.revoked || o.x_mitre_deprecated) continue;
  const r = ref(o);
  if (!r) continue;
  const tactics = Array.from(
    new Set(
      (o.kill_chain_phases || [])
        .filter((p) => p.kill_chain_name === 'mitre-attack')
        .map((p) => tacticName[p.phase_name])
        .filter(Boolean),
    ),
  );
  byId[r.id] = { id: r.id, name: o.name, tactics, url: r.url, summary: clean(o.description), sub: !!o.x_mitre_is_subtechnique };
}

// Curated overlay: reuse the existing attack-overlay.ts if present; otherwise
// migrate it out of the legacy ATTACK_TECHNIQUES array in references.ts (one time).
const overlayPath = path.join(ROOT, 'src/data/attack-overlay.ts');
let overlay = {};
if (fs.existsSync(overlayPath)) {
  const m = fs.readFileSync(overlayPath, 'utf8').match(/ATTACK_OVERLAY[^=]*=\s*(\{[\s\S]*?\n\};)/);
  if (m) overlay = JSON.parse(m[1].replace(/;\s*$/, '').replace(/,(\s*[\]}])/g, '$1'));
} else {
  const src = fs.readFileSync(path.join(ROOT, 'src/data/references.ts'), 'utf8');
  const m = src.match(/ATTACK_TECHNIQUES:\s*AttackTechnique\[\]\s*=\s*(\[[\s\S]*?\n\];)/);
  if (m) {
    const arr = JSON.parse(m[1].replace(/;\s*$/, '').replace(/,(\s*[\]}])/g, '$1'));
    for (const c of arr) {
      const o = {};
      if (c.glossarySlug) o.glossarySlug = c.glossarySlug;
      if (c.summary) o.summary = c.summary;
      if (c.significance) o.significance = c.significance;
      if (c.example) o.example = c.example;
      overlay[c.id] = o;
    }
  }
}

// Final set: every base technique, plus any curated sub-technique by id.
const wanted = new Set(Object.values(byId).filter((t) => !t.sub).map((t) => t.id));
for (const id of Object.keys(overlay)) wanted.add(id);

const out = [];
for (const id of [...wanted].sort()) {
  const t = byId[id];
  if (!t) { console.warn('  overlay id missing from STIX (skipped):', id); continue; }
  out.push({ id: t.id, name: t.name, tactics: t.tactics, url: t.url, summary: t.summary });
}

const genFile =
  '// AUTO-GENERATED by scripts/gen-attack-map.mjs from the MITRE ATT&CK Enterprise\n' +
  `// STIX bundle (v${(objects.find((o) => o.type === 'x-mitre-collection') || {}).x_mitre_version || '?'}). Do not edit by hand — re-run the script to refresh.\n\n` +
  '/** Tactics in kill-chain order, straight from the ATT&CK matrix definition. */\n' +
  'export const ATTACK_TACTIC_ORDER: string[] = ' + JSON.stringify(tacticOrder, null, 2) + ';\n\n' +
  'export interface GeneratedTechnique {\n  id: string;\n  name: string;\n  tactics: string[];\n  url: string;\n  summary: string;\n}\n\n' +
  'export const ATTACK_GENERATED: GeneratedTechnique[] = ' + JSON.stringify(out, null, 2) + ';\n';
fs.writeFileSync(path.join(ROOT, 'src/data/attack-techniques.generated.ts'), genFile);

if (!fs.existsSync(overlayPath)) {
  const ovHeader =
    '// Curated overlay for ATT&CK techniques — glossary links + bespoke DFIR framing\n' +
    '// (summary / significance / example). Hand-maintained; merged onto the generated\n' +
    '// MITRE data in references.ts. Add an entry to enrich a technique’s detail page.\n' +
    'export interface AttackOverlay {\n  glossarySlug?: string;\n  summary?: string;\n  significance?: string;\n  example?: string;\n}\n\n' +
    'export const ATTACK_OVERLAY: Record<string, AttackOverlay> = ';
  fs.writeFileSync(overlayPath, ovHeader + JSON.stringify(overlay, null, 2) + ';\n');
}

console.log('tactics:', Object.values(tacticName).join(' · '));
console.log('wrote', out.length, 'techniques;', Object.keys(overlay).length, 'curated overlay entries');
