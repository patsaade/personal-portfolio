// DFIR toolkit — the single source of truth for the Tools index (/tools/) and the
// per-tool detail pages (/tools/[slug]/). Grouped by the platform a tool runs on.
// Each tool carries a short `use` (card one-liner), a `fn` function tag, a `cost`
// licensing tag, a `tags` array of descriptive keywords (shown on the detail page
// and folded into search), and — for the detail page, in the glossary format —
// `what` it is, `why` it matters in DFIR, and an `example` of it in practice.
// `core` marks the tools in the day-to-day kit. `references` adds verified,
// genuinely distinct docs/cheat-sheets/guides beyond the official site (`url`) —
// every URL was machine-verified live at authoring time, same standard as the
// glossary's TERM_REFERENCES. `install`/`syntax` are verified against the tool's
// own docs/README/help output — `syntax` is omitted for GUI-only tools rather
// than forcing a fabricated CLI example. Content is factual (public tools); add
// a tool by appending to its platform.

import type { IconName } from '../components/Icon.astro';

export type ToolCost = 'Open source' | 'Free' | 'Freemium';

export interface ToolReference {
  name: string;
  url: string;
}

export interface Tool {
  name: string;
  slug: string;
  use: string; // short one-liner for the card
  fn: string; // function/category tag
  tags: string[]; // descriptive keyword tags (search + detail page)
  url: string; // official site
  cost: ToolCost;
  core?: boolean; // in the day-to-day kit
  what?: string; // "What it is" — definition
  why?: string; // "Why it matters" — DFIR significance
  example?: string; // "In practice" — concrete example
  references?: ToolReference[]; // additional verified docs/guides beyond the official site
  install?: string; // how to get it (verified)
  syntax?: string; // a representative command-line example (verified; omitted for GUI-only tools)
  // Glossary slugs this tool directly implements (e.g. WinPmem -> memory-acquisition).
  // Curated conservatively: only genuinely obvious, factual matches — see the
  // "In the glossary" section on the tool's detail page and the reverse "Tools"
  // section on the matching glossary term page.
  glossarySlugs?: string[];
}

export interface ToolPlatform {
  id: string;
  title: string;
  icon: IconName;
  blurb: string;
  tools: Tool[];
}

