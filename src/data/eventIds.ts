// Windows Security auditing log + Sysmon event-ID reference — the single
// source of truth for the /event-ids/ index and per-event detail pages
// (/event-ids/[slug]/). Every entry was researched then independently
// fact-checked against live, authoritative documentation (Microsoft Learn's
// per-event auditing pages, the official Sysinternals Sysmon docs) before
// being included -- entries that couldn't be confirmed were dropped rather
// than guessed at. `slug` is `{source}-{id}` (e.g. "security-4624",
// "sysmon-1") since Security and Sysmon event IDs occupy separate numbering
// and can collide. `relatedEventIds` and `attackTechniques` are bare IDs
// resolved against this same dataset / the site's ATT&CK dataset at render
// time (see event-ids/[slug].astro) -- an ID that doesn't resolve just
// renders as plain text or links off-site, it doesn't break the page.
// `keyFields` -- the specific fields an analyst should actually pull out of
// the raw event when triaging it (not an exhaustive field dump) -- was
// researched the same way, sourced from each entry's own `references` URLs;
// an entry with nothing beyond ordinary boilerplate fields carries an empty
// array rather than padded filler. `example` -- a short, generic illustrative
// occurrence (redacted-style usernames, RFC 5737 documentation-range IPs
// only, real legitimate process names where relevant) -- follows the same
// no-guessing discipline; an entry the research pass couldn't ground in the
// event's own documented fields just omits the field rather than inventing
// filler, so it's optional and absent on a few entries.
